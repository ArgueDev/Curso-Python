# Operadores
print(3 + 4) # Suma
print(3 - 4) # Resta
print(3 * 4) # Multiplicacion
print(3 / 4) # Division
print(10 % 2) # Modulo - Resto
print(10 // 3) # Division con resultado entero
print(2 ** 3) # Exponente

print("Hola " + "Python")
