# Type Hints

string_variable = 'My String'
print(string_variable)
print(type(string_variable))

string_variable = 5
print(string_variable)
print(type(string_variable))

typed_variable: str = 'My typed string'
print(typed_variable)
print(type(typed_variable))

typed_variable = 5
print(typed_variable)
print(type(typed_variable))